const today = new Date();
const addDays = (d: number) => {
  const t = new Date(today);
  t.setDate(t.getDate() + d);
  return t.toISOString();
};

const mkSub = (orderId: string, productId: string, part: string, point: number, assignedTo: string | undefined, status: SubTask["status"]): SubTask => ({
  id: `${orderId}-${part}`,
  orderId,
  productId,
  partName: part as SubTask["partName"],
  point,
  assignedTo,
  status,
  startedAt: assignedTo && status !== "Waiting" ? addDays(-2) : undefined,
  finishedAt: status === "Selesai" ? addDays(-1) : undefined,
});

